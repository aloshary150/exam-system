module.exports = async pool => {
  const data = [
    ["كم عدد أركان الإسلام؟", ["3", "4", "5", "6"], 2],
    ["ما هو أول ركن في الإسلام؟", ["الصلاة", "الشهادة", "الزكاة", "الصوم"], 1],
    ["كم عدد الصلوات المفروضة؟", ["3", "4", "5", "6"], 2],
    ["في أي شهر يصوم المسلمون؟", ["شعبان", "رمضان", "رجب", "ذو الحجة"], 1],
    ["ما هي قبلة المسلمين؟", ["المدينة", "القدس", "الكعبة", "مكة"], 2],
    ["من هو خاتم الأنبياء؟", ["موسى", "عيسى", "محمد ﷺ", "إبراهيم"], 2],
    ["كم مرة يحج المسلم في العمر؟", ["مرة", "مرتين", "ثلاث", "كل سنة"], 0],
    ["ما هو كتاب المسلمين؟", ["التوراة", "الإنجيل", "القرآن", "الزبور"], 2],
    ["أين نزل الوحي أول مرة؟", ["المسجد", "البيت", "غار حراء", "المدينة"], 2],
    ["ما اسم أم النبي ﷺ؟", ["خديجة", "آمنة", "حفصة", "عائشة"], 1]
  ];

  for (const q of data) {
    const r = await pool.query(
      "INSERT INTO questions(question) VALUES($1) RETURNING id",
      [q[0]]
    );
    for (let i = 0; i < 4; i++) {
      await pool.query(
        "INSERT INTO answers(question_id,answer,is_correct) VALUES($1,$2,$3)",
        [r.rows[0].id, q[1][i], i === q[2]]
      );
    }
  }
};
